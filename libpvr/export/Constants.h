namespace Colors {
  inline Color one()
  { return Color(1.0); }
  inline Color zero()
  { return Color(0.0); }
  inline Color red()
  { return Color(1.0, 0.0, 0.0); }
}

//----------------------------------------------------------------------------//

namespace Vectors {
  inline Vector zero() {
    return Vector(0.0);
  }
}

//----------------------------------------------------------------------------//

namespace Bounds {
  inline BBox zeroOne() {
    return BBox(Vector(0.0), Vector(1.0));
  }
}

